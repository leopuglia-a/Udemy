from math import *

print(ceil(3.14))	# maior inteiro

print(fabs(-4))		# valor absoluto

print(floor(3.14))	# menor inteiro

print(exp(2))		# e elevado a 2

log10(10)			# logaritmo base 10
log(1,10)			# log 1 base 10

pow(2,10)			# 2 elevado a 10

sqrt(25)			# raiz de 25

cos(0)				# cosseno de 0
sin(30)				# seno de 30 em radianos

